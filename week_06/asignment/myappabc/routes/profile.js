var express = require('express');
var router = express.Router();

/* GET profile page. */
router.get('/', function(req, res, next) {

// object
const profile ={
    message: 'Hello World',
    name: 'John Doe',
    age: 30,
    email: "Johndoe@exeample.com"
}
  res.send(`
      <html>
      <head>
        <title>Profile Page</title>
      </head>
      <body>
        <h1>Profile Information</h1>
        <p>Message: ${profile.message}</p>
        <p>Name: ${profile.name}</p>
        <p>Age: ${profile.age}</p>
         <p>Email: ${profile.email}</p>
        <script>
          console.log(${JSON.stringify(profile)}); // Menampilkan objek di konsol browser
        </script>
      </body>
    </html>
    `);
});

module.exports = router;